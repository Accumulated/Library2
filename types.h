#ifndef TYPES_H_
#define TYPES_H_

typedef unsigned char u8; 		// 8 bits
typedef unsigned short int u16; // 16 bits
typedef unsigned long int u32; 	// 32 bits
typedef signed char s8	;		// 8 bits
typedef signed short int s16;	// 16 bits
typedef signed long int s32	;	// 32 bits
typedef float f32	;	// 32 bits float
typedef double f64;		// 64 bits float

#endif

